                      <h4>General Guidelines</h4><br>
                      <ul style="text-align: left"> 				  
					  
                        <li>Authors are invited to submit both an abstract and an extended abstract based on their research findings for oral presentations at the conference.</li>
                        <li>During the initial submission, authors should upload both the abstract and the extended abstract to the <strong><big> CMT system</big></strong>.</li>
                        <li>Abstracts and extended abstracts that include author details will not undergo review. After submission, author names, affiliations, and acknowledgments will be removed for the purpose of review before being sent to the reviewers.</li>
                        <li>Previously presented or published research will not be considered.</li>
                        <li>Any identified instances of plagiarism will lead to an immediate rejection of the abstract without further evaluation.</li>
                        <li>Prior to submission, authors are strongly advised to utilize an authentic or freely available online plagiarism checker to review their abstracts and extended abstracts.</li>
                        <li>Authors must categorize their abstracts under one of the <strong><big>five specified Themes</big></strong>. However, the Editorial Board retains the right to reassign a selected abstract to a more suitable theme if deemed necessary.</li>
                        <li>To streamline the reviewing and editing process, authors must submit a signed Author Declaration Form, endorsed by all authors, along with the initial submission. The final assessment and editing phases will commence only upon receipt of all relevant documents and information.</li>
						
					  </ul>
					 <br>
					 
					 
					 <h4>Abstract Guidelines</h4><br>
                      <ul style="text-align: left">
                        <li>Abstracts for the 15th Annual Science Research Session (ASRS 2026) will be published in the conference proceedings. Please adhere to the following formatting guidelines:</li>
                        <br><strong><i>Paper Specifications:</i></strong>
                        <li>Paper size: A4</li>
						<li>Margins: Left margin 3.0 cm, all other margins (top, bottom, and right) 2.5 cm</li>
                        <li>Font: Times New Roman</li>
                        <li>Font size: 12 (except for authors' affiliations)</li>
                        <li>Line spacing: Single</li>
                        <li>Page limit: One page</li>
                      
					  <br><strong><i>Language and Software:</i></strong>
                          <li>All abstracts must be in English and prepared using MS Word 2007 or later.</li>

                      <br><li>Abstract Content: Please use the provided Abstract template and structure your abstract as follows:</li>
                      
					  <b>Title:</b>
                      <li>Brief and descriptive, reflecting the study</li>
                      <li>Bold, uppercase letters, center-aligned</li>
                      <li>Use uppercase italics for scientific names of organisms, with common names in parentheses</li>
                      <li>Two-line spaces between the Abstract number and title</li>
                      <li>Single line space between title and author names</li>
					  
					  <b>Author(s):</b>
                        <li>Initials before surname (last name)</li>
                        <li>Bold, lowercase letters, center-aligned</li>
                        <li>Initials followed by a full-stop, no space</li>
                        <li>Corresponding author (Primary contact) marked with an asterisk (*)</li>
                        <li>Single space between last initial and surname</li>
                        <li>No line space between author names and affiliations</li>

                      <b>Affiliation(s) of Author(s):</b>
                        <li>Italics, font size 11, lowercase letters (with necessary uppercase)</li>
                        <li>Different affiliations numbered with superscript</li>
                        <li>Each affiliation on a separate line</li>
                        <li>Superscript number at beginning of affiliation and end of author's name</li>
                        <li>Single line space between affiliations and abstract text</li>

                     <b>Abstract text:</b>
                       <li>Single paragraph with an introduction, objectives, methodology, results, brief discussion, and conclusion(s)</li>
                       <li>Justified alignment</li>
                       <li>No sub-headings, figures, or tables</li>
                       <li>No references</li>

                     <b>Keywords:</b>
                        <li>Up to 5 keywords, separated by commas and a space</li>
                        <li>Each keyword starts with an uppercase letter</li>
                        <li>Single line space after the abstract text</li>

                    <b>Acknowledgement:</b>
                         <li>Acknowledge funding organizations with grant numbers in parentheses</li>
                         <li>Italics, a single sentence</li>
                         <li>Single line space after the Keywords</li>

                    <br><b><i>Additional Notes:</i></b>
                       <li>Full form of abbreviations and acronyms on first use (except standard abbreviations)</li>
                       <li>Do not use abbreviations and acronyms in the title, unless necessary</li>
                       <li>Non-English and technical terms should be in italics.</li>

                      </ul>
						
						
						<br><h4>Extended Abstract Guidelines</h4><br>
                        <ul style="text-align: left">
                           <li>Please be aware that the extended abstract will not be included in the Proceedings. Its sole purpose is for the review process.</li>
                           <li>When preparing the extended abstract, adhere to the subsequent formatting instructions:</li>
                           
                           <strong>Page size:</strong> A4<br>
                           <strong>Margins:</strong> Maintain 2.5 cm margins on all sides (top, bottom, left, right)<br>
                           <strong>Font:</strong> Times New Roman<br>
                           <strong>Font size:</strong> 12<br>
                           <strong>Line spacing:</strong> Single<br>
                           <strong>Page count:</strong> 3-4 pages<br>
                           <strong>Page numbering:</strong> Place at the bottom right corner<br>
                           <strong>Title:</strong> Include the title, author(s) names, and their respective affiliations exactly as presented in the abstract.<br>
                           <br>
                           <li>Arrange the content under the subsequent subheadings:</li>
                           <strong>1.	Title:</strong> Followed by the authors' names and affiliations<br>
                           <strong>2.	Introduction:</strong> Clearly state the objectives<br>
                           <strong>3.	Materials and Methods</strong><br>
                           <strong>4.	Results</strong><br>
                           <strong>5.	Discussion</strong><br>
                           <strong>6.	Conclusion(s):</strong> Present the outcomes<br>
                          <br><li>If applicable, ensure that any Figures and Tables are clear and fit within the designated page limit.</li>
                        </ul>
						
						
