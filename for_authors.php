                      <h4>Abstract Review Process</h4><br>
                      <ul style="text-align: left">
                        <li>All submitted abstracts will  undergo a double-blind peer review conducted by a panel of internal and  external expertise in sciences. </li>
                        <li>Results of the abstract  review will be sent to the author (Conditions apply if there is an emergency).</li>
                        <li>Failure to remove template  text from your paper may result in your paper not being published. </li>
                        <li>Notices of acceptance or  rejection of the paper will be sent to the author with reviewer comments. </li>
                        <li>If revisions are required  according to the reviewer comments, the revised abstract must be sent back within the given deadline. </li>
                        <li>If your abstract is accepted, you will be invited to register for the  Conference. </li>
